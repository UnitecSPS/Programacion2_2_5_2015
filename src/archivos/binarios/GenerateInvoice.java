    private void initFormaPago() {
        cbFormaPago.setModel(new DefaultComboBoxModel(PaymentType.values()));
    }

    private void addRowToTable(int codigo, String nombre, int cant, double precio) {
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object row[] = {codigo,nombre,cant,precio,cant*precio};
        model.addRow(row);
    }

    private void updateSummary(double monto) {
        sum(txtSt, monto);
        double imp = monto * 0.15;
        sum(txtImpuesto, imp);
        sum(txtTotal, monto+imp);
    }
    
    private void sum(JTextField field, double monto){
        double st = Double.parseDouble(field.getText());
        field.setText(""+(st+monto));
    }
