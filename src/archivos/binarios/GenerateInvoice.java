    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            String codp = JOptionPane.showInputDialog("Ingrese Codigo: ");
            InvoiceItem item = Menu.market.getProduct(Integer.parseInt(codp));
            
            if(item!= null && item.cantidad > 0){
                String cantm = JOptionPane.showInputDialog("Cantidad: ");
                int cant = Integer.parseInt(cantm);
                if( cant <= item.cantidad){
                    addRowToTable(item.codigo, item.nombre,
                            cant, item.precio);
                    updateSummary(cant*item.precio);
                }
                else
                    JOptionPane.showMessageDialog(this, "Existencia Insuficiente");
            }
            else
                JOptionPane.showMessageDialog(this, "No Hay Existencia de ese producto");
        }catch(Exception e){
            Menu.exceptionOptionPane(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


